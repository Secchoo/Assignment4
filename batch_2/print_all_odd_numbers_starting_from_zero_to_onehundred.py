# Initialize the variable i to 0
i = 0

# Loop while i is less than or equal to 100
while i <= 100:
    # Check if i is an odd number
    if i % 2 != 0:
        # Print the odd number followed by a space
        print(i, end=' ')
    # Increment i by 1
    i += 1

# Print a newline character at the end
print()